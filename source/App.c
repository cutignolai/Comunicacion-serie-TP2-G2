/* Función que se llama 1 vez, al comienzo del programa */
void App_Init (void)
{
    CAN_Init();
}

/* Función que se llama constantemente en un ciclo infinito */
void App_Run (void)
{
    CAN_DataFrame frame;

	frame.ID     = MY_BOARD_ID;

	CAN_WriteTxMB( 0, &frame);

	static volatile int i;

	i = 800000;

	while(i > 0)
	{
		--i;
	}




    
}


